import {
  collection,
  addDoc,
  getDocs,
  query,
  where,
  orderBy,
  serverTimestamp,
  doc,
  getDoc,
  updateDoc
} from 'firebase/firestore';
import { ref, uploadBytes, getDownloadURL } from 'firebase/storage';
import { db, storage } from '../firebase/config';

// ==================== STRATEGIES ====================

export const strategiesCollection = collection(db, 'strategies');

export const saveStrategy = async (data) => {
  return await addDoc(strategiesCollection, {
    businessName: data.businessName || '',
    targetAudience: data.targetAudience || '',
    businessCountry: data.businessCountry || '',
    currentMarketing: data.currentMarketing || '',
    userEmail: data.userEmail || null,
    userName: data.userName || null,
    strategy: data.strategy || {},
    marketingMaterialsLink: data.marketingMaterialsLink || '',
    marketingMaterialsFiles: data.marketingMaterialsFiles || [],
    status: data.status || 'generated', // generated, viewed, exported, emailed
    paymentStatus: data.paymentStatus || 'pending', // pending, paid, refunded
    paymentAmount: data.paymentAmount || null,
    paymentMethod: data.paymentMethod || null,
    createdAt: serverTimestamp(),
    updatedAt: serverTimestamp()
  });
};

export const getAllStrategies = async (filters = {}) => {
  let q = strategiesCollection;

  if (filters.status) {
    q = query(q, where('status', '==', filters.status));
  }

  if (filters.paymentStatus) {
    q = query(q, where('paymentStatus', '==', filters.paymentStatus));
  }

  q = query(q, orderBy('createdAt', 'desc'));

  const snapshot = await getDocs(q);
  return snapshot.docs.map(doc => ({
    id: doc.id,
    ...doc.data()
  }));
};

export const getStrategyById = async (id) => {
  const docRef = doc(db, 'strategies', id);
  const docSnap = await getDoc(docRef);
  if (docSnap.exists()) {
    return { id: docSnap.id, ...docSnap.data() };
  }
  return null;
};

export const updateStrategyStatus = async (id, data) => {
  const docRef = doc(db, 'strategies', id);
  return await updateDoc(docRef, {
    ...data,
    updatedAt: serverTimestamp()
  });
};

export const getStrategiesByUser = async (email) => {
  const q = query(
    strategiesCollection,
    where('userEmail', '==', email),
    orderBy('createdAt', 'desc')
  );
  const snapshot = await getDocs(q);
  return snapshot.docs.map(doc => ({
    id: doc.id,
    ...doc.data()
  }));
};

