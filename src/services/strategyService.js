// ==================== STRATEGY EMAIL DELIVERY ====================

export const requestStrategyDelivery = async (strategyId, userEmail) => {
  const FIREBASE_FUNCTION_URL = 'https://us-central1-freeflow-media.cloudfunctions.net/deliverStrategyViaEmail';
  
  try {
    const response = await fetch(FIREBASE_FUNCTION_URL, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        strategyId,
        userEmail
      })
    });

    if (!response.ok) {
      throw new Error('Failed to send strategy email');
    }

    return await response.json();
  } catch (error) {
    console.error('Strategy delivery failed:', error);
    throw error;
  }
};

// ==================== FILE UPLOADS ====================

export const uploadStrategyFiles = async (files) => {
  const uploadPromises = files.map(async (file) => {
    const fileRef = ref(storage, `strategies/${Date.now()}-${file.name}`);
    const snapshot = await uploadBytes(fileRef, file);
    return await getDownloadURL(snapshot.ref);
  });
  return Promise.all(uploadPromises);
};

// ==================== ANALYTICS ====================

export const getStrategyStats = async () => {
  const snapshot = await getDocs(strategiesCollection);
  const strategies = snapshot.docs.map(doc => doc.data());

  const stats = {
    total: strategies.length,
    generated: strategies.filter(s => s.status === 'generated').length,
    viewed: strategies.filter(s => s.status === 'viewed').length,
    exported: strategies.filter(s => s.status === 'exported').length,
    emailed: strategies.filter(s => s.status === 'emailed').length,
    paid: strategies.filter(s => s.paymentStatus === 'paid').length,
    pending: strategies.filter(s => s.paymentStatus === 'pending').length,
    totalRevenue: strategies
      .filter(s => s.paymentStatus === 'paid')
      .reduce((sum, s) => sum + (s.paymentAmount || 0), 0)
  };

  return stats;
};
